class ComplexRewardNoPU(
    BringObjectiThorBaseConfig,
    BringObjectMixInPPOConfig,
    BringObjectMixInSimpleGRUConfig,
):
    """An Object Navigation experiment configuration in iThor with RGB
    input."""
    NOISE_LEVEL = 0
    distance_thr = 1.5 # is this a good number?
    SENSORS = [
        RGBSensorThor(
            height=BringObjectiThorBaseConfig.SCREEN_SIZE,
            width=BringObjectiThorBaseConfig.SCREEN_SIZE,
            use_resnet_normalization=True,
            uuid="rgb_lowres",
        ),
        # DepthSensorThor(
        #     height=BringObjectiThorBaseConfig.SCREEN_SIZE,
        #     width=BringObjectiThorBaseConfig.SCREEN_SIZE,
        #     use_normalization=True,
        #     uuid="depth_lowres",
        # ),
    ]

    MAX_STEPS = 200

    TASK_SAMPLER = ExploreTaskSampler
    TASK_TYPE = ExploreTask

    NUM_PROCESSES = 40

    OBJECT_TYPES = TRAIN_OBJECTS + TEST_OBJECTS

    VISUALIZE = False



    def __init__(self):
        super().__init__()
        self.REWARD_CONFIG['exploration_reward'] = 0.05 # is this too big?
        self.REWARD_CONFIG['object_reward'] = 0.1 # is this too big?



    @classmethod
    def create_model(cls, **kwargs) -> nn.Module:
        return ExplorationModel(
            action_space=gym.spaces.Discrete(
                len(cls.TASK_TYPE.class_action_names())
            ),
            observation_space=kwargs["sensor_preprocessor_graph"].observation_spaces,
            hidden_size=512,
            visualize=cls.VISUALIZE
        )

    @classmethod
    def tag(cls):
        return cls.__name__
